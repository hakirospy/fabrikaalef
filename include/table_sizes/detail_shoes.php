		 Верхняя одежда для детей<br>
 <br>
		 При определении детского размера, российский стандарт предлагает ориентироваться на рост ребенка, при этом шаг между маркировками составляет 6 см. У малышей младше 2-х лет рост замеряют лежа и только потом – стоя. Как правило, для детей возрастом больше пяти лет этот особый способ определения размера становится не очень качественным.<br>
 <br>
		 Модную детскую верхнюю одежду, также часто выбирают согласно возрасту. В целом, как только малыш начинает активно ходить, лучше учитывать при выборе основные мерки, такие как объем груди, объем талии, объем бедер и длина рукава.<br>
